import request from '@/common/utils/request'

let BaseUrl = "/sportScoreInfo"

/**
 * 列表
 * @param data
 * @returns {*}
 */
export function queryPageSportScoreInfo(data) {
    return request({
        url: BaseUrl + '/page',
        method: 'post',
        data: data,
    })
}

/**
 * 创建/修改
 * @returns {*}
 * @param data
 */
export function saveSportScoreInfo(data) {
    return request({
        url: BaseUrl + '/save',
        method: 'post',
        data: data,
    })
}

/**
 * 删除
 * @returns {*}
 * @param params
 */
export function deleteSportScoreInfo(params) {
    return request({
        url: BaseUrl + '/delete',
        method: 'delete',
        params: params,
    })
}

